        <h2>A Flying Pig</h2>
        <br>
        <br>
        <p>When I was on my mission in the Philippines it became apparent there were two seasons,
        a wet season and a dry season. But that is not to say it did not rain during the dry season -
        it just rains on time and a little less during that season. During the dry season on Mindanao
        it started to rain about 2:30-3:00 each afternoon and rained for about two hours. It was a nice way to
        cool off the afternoon. During the wet season it rains whenever and for however long; it rained long
        and hard and umbrellas did no good, the rain came through them and created a fine spray. </p>
        <br>
            <img src="images/flyingpig.jpg" width="200" height="400" alt="" class="float">
        <p>It was during one such rainy season I observed some of the strangest things I had ever seen. The
        rain started coming down and did not stop; soon it had nowhere to go. Mindanao is an island and I
        have never seen rain back up so fast that it could not drain off the beaches, but that is exactly what
        was happening. </p>
        <br>
        <p>Soon we were three feet deep in water and much more in some places and the water was still rising.
        The rain was still coming down. </p>
        <br>
        <p>We would ride around in public transportation, and there were only two types in this city. One
        was a motorcycle with a third wheel attached to the back tire and a bench with a canopy over the top.
        These were two cycle motorcycle engines, the type of engines where you mix gas and oil together and it
        burns at the same time, just like in a lawn mower or weed whacker. Needless to say, they do not move
        very fast and these tricycles did not work very well in deep water, if at all. </p>
        <br>
        <p>The other form of transportation is old WWII jeeps painted and decorated with bright colors and decals.
        They are called jeepney and are the major sources of public transportation in the Philippines. Some of
        the vehicles are cared for much better than others and some are not cared for at all; and some it seems
        are still all made from the original parts and held together with spit and wire. </p>
        <br>
        <p>It was from one of the jeepneys I watched a man sitting on the top of a sign post fishing in the rain
        during this flood, which in and of itself is strange but I only mention it because I watched him pull a
        fish from the flood water. I was dumbfounded and laughed to myself not wanting to make a big deal about
        it in case this was a normal thing on the island. But this is all leading up to something even more strange. </p>
        <br>
        <p>Further down the road we stopped in the middle of a bridge and water was coming over the top of the roadway.
        I could see a lot of water coming down the creek at great speed and from somewhere there was a loud noise. This
        noise was getting louder, and I began to realize it was a pig squealing as hard as it could. I could see this
        pig coming down this creek straight at us, then it disappeared, and all seemed to go silent.  </p>
        <br>
        <p>All of a sudden, the water started to rise on the bridge, and it was rising fast. We could see the water top
        the tires and soon it was coming into the back of the jeepney and people were starting to stand on the seats wondering
        what to do. Just then there was a loud POP and the water was receding and squealing started again. </p>
        <br>
        <p>That poor pig was stuck in the culvert and had plugged it up, so much so that it had backed the water up over the
        bridge and into the vehicle. If you tried you could just picture this poor pig being pushed little by little, slowly
        down through this culvert until it popped out of the other end. That pop sent the pig flying through the air, out of
        the drain and then back into the creek. </p>
        <br>
        <p>This big, fat piggy did not swim - in fact I do not think pigs can swim as I watched the way this one was rotating
        head to toe and rolling from side to side. I think it was just more or less swiftly being moved down the side of the
        hill as it moved back and forth from either side of the banks of this fast-moving stream. Even if pigs could swim this
        one never had a chance. </p>
        <br>
        <p>That poor pig was stuck in the culvert and had plugged it up, so much so that it had backed the water up over the
        bridge and into the vehicle. If you tried you could just picture this poor pig being pushed little by little, slowly
        down through this culvert until it popped out of the other end. That pop sent the pig flying through the air, out of
        the drain and then back into the creek. </p>
